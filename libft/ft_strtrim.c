#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	char	*d;
	size_t	l;

	if (!s1 || !set)
		return (NULL);
	while (*s1 && ft_strchr(set, *s1))
		s1++;
	l = ft_strlen(s1);
	while (ft_strchr(set, s1[l]) && l)
		l--;
	d = ft_substr((char *)s1, 0, l + 1);
	return (d);
}
